#Programa para calcular el area en un cuadrado en python
#El simbolo (*) significa multiplicacion en python

#creamos dos variabes para cada lado con input para preguntar
#y float para valores con punto decimal

lado1 = float(input('Dame el lado 1:'))
lado2 = float(input('Dame el lado 2:'))

#Creamos variable para hacer la formula :)
#dentro de la variable multiplicamos el lado1 por el lado 2
area = lado1 * lado2

#Por ultimo mostramos en pantalla el resultado con un print y dentro la vareable
#area

print(area)
